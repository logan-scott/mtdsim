"""One-shot regeneration of every figure, table, and the run manifest.

    python -m mtdsim.experiments.run_all --config configs/paper.yaml

Runs all five experiments from a single config with no manual steps, writes
grayscale figures to ``figures/``, CSV+LaTeX tables to ``tables/``, raw and
summary data under ``results/``, a human-readable ``figures/CAPTIONS.md``, and a
JSON run manifest (config, seed, git commit, env, output hashes) so the run is
fully reproducible and auditable.
"""

from __future__ import annotations

import argparse
from collections.abc import Callable
from pathlib import Path
from typing import Any

from ..config import Config, load_config
from ..manifest import build_manifest, write_manifest
from . import (
    ablation,
    adaptive,
    baseline,
    cost_weight_sensitivity,
    decoy_ratio_sweep,
    diversity_channel_decomposition,
    frequency_by_technique_count,
    frequency_sweep,
    parallelism_sweep,
    sensitivity,
)
from .common import ExperimentOutput, OutputLayout

# Ordered registry of experiments (name -> run function).
EXPERIMENTS: dict[str, Callable[..., ExperimentOutput]] = {
    "baseline": baseline.run,
    "frequency_sweep": frequency_sweep.run,
    "frequency_by_technique_count": frequency_by_technique_count.run,
    "cost_weight_sensitivity": cost_weight_sensitivity.run,
    "decoy_ratio_sweep": decoy_ratio_sweep.run,
    "ablation": ablation.run,
    "diversity_channel_decomposition": diversity_channel_decomposition.run,
    "adaptive_vs_nonadaptive": adaptive.run,
    "parallelism_sweep": parallelism_sweep.run,
    "sensitivity": sensitivity.run,
}


def run_all(
    cfg: Config,
    layout: OutputLayout,
    *,
    parallel: bool = False,
    only: list[str] | None = None,
    config_path: str | None = None,
    verbose: bool = True,
) -> dict[str, Any]:
    """Execute the requested experiments and write all artifacts + manifest."""
    selected = list(EXPERIMENTS) if not only else only
    unknown = set(selected) - set(EXPERIMENTS)
    if unknown:
        raise ValueError(f"unknown experiments requested: {sorted(unknown)}")

    outputs: list[ExperimentOutput] = []
    exp_meta: dict[str, Any] = {}
    captions: dict[str, str] = {}
    all_files: list[str] = []

    for name in selected:
        if verbose:
            print(f"[run_all] running experiment: {name} ...", flush=True)
        out = EXPERIMENTS[name](cfg, layout, parallel=parallel)
        outputs.append(out)
        all_files.extend(out.output_files())
        captions.update(out.captions())
        exp_meta[name] = {
            "n_cells": int(len(out.summary)),
            "tables": [t for t in out.tables],
            "figures": [f.get("paths", []) for f in out.figures],
            "extra": out.extra,
        }

    _write_captions(layout.figures / "CAPTIONS.md", captions)
    all_files.append(str(layout.figures / "CAPTIONS.md"))

    manifest = build_manifest(
        config_fingerprint=cfg.fingerprint(),
        config_path=config_path,
        seed=cfg.seed,
        experiments=exp_meta,
        output_files=all_files,
    )
    manifest_path = write_manifest(manifest, layout.results / "manifest.json")
    if verbose:
        print(f"[run_all] wrote manifest: {manifest_path}", flush=True)
        print(f"[run_all] figures -> {layout.figures}  tables -> {layout.tables}", flush=True)
    return manifest


def _write_captions(path: Path, captions: dict[str, str]) -> None:
    lines = ["# Figure captions", "", "Auto-generated by `mtdsim.experiments.run_all`.", ""]
    for name in sorted(captions):
        lines.append(f"## `{name}`")
        lines.append("")
        lines.append(captions[name])
        lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")


def build_arg_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        prog="mtdsim.experiments.run_all",
        description="Regenerate all MTD-vs-AI figures, tables, and the manifest.",
    )
    p.add_argument("--config", default="configs/paper.yaml", help="path to YAML config")
    p.add_argument("--outdir", default="results", help="directory for raw/summary/manifest")
    p.add_argument("--figures", default="figures", help="directory for figures")
    p.add_argument("--tables", default="tables", help="directory for tables")
    p.add_argument("--seed", type=int, default=None, help="override master seed")
    p.add_argument("--trials", type=int, default=None, help="override engine.n_trials")
    p.add_argument(
        "--only",
        default=None,
        help="comma-separated subset of experiments to run (default: all)",
    )
    p.add_argument("--parallel", action="store_true", help="run trials across processes")
    p.add_argument("--quiet", action="store_true", help="suppress progress output")
    return p


def main(argv: list[str] | None = None) -> int:
    args = build_arg_parser().parse_args(argv)
    cfg = load_config(args.config)
    overrides: dict[str, Any] = {}
    if args.seed is not None:
        overrides["seed"] = args.seed
    if args.trials is not None:
        overrides["engine"] = {"n_trials": args.trials}
    if overrides:
        cfg = cfg.with_overrides(**overrides)

    layout = OutputLayout.create(args.outdir, args.figures, args.tables)
    only = [s.strip() for s in args.only.split(",")] if args.only else None
    run_all(
        cfg,
        layout,
        parallel=args.parallel,
        only=only,
        config_path=args.config,
        verbose=not args.quiet,
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
